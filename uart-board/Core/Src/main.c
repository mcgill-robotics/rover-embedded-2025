static void echo_serial_port(uint8_t itf, uint8_t buf[], uint32_t count) {
  for (uint32_t i = 0; i < count; i++) {
    tud_cdc_n_write_char(itf, buf[i]);
  }
  tud_cdc_n_write_flush(itf);
}

